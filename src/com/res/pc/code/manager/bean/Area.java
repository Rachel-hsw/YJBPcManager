package com.res.pc.code.manager.bean;

public class Area {
private String id;
private String PCityname;
private String Pparent_id;
private String Ptype;
private String PStatus;
private String Fuserid;
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getPCityname() {
	return PCityname;
}
public void setPCityname(String pCityname) {
	PCityname = pCityname;
}
public String getPparent_id() {
	return Pparent_id;
}
public void setPparent_id(String pparent_id) {
	Pparent_id = pparent_id;
}
public String getPtype() {
	return Ptype;
}
public void setPtype(String ptype) {
	Ptype = ptype;
}
public String getPStatus() {
	return PStatus;
}
public void setPStatus(String pStatus) {
	PStatus = pStatus;
}
public String getFuserid() {
	return Fuserid;
}
public void setFuserid(String fuserid) {
	Fuserid = fuserid;
}

}
